//Даны действительные числа a1, …, an, b1, …, b¬¬n.
//Вычислить(a1 + bn)(a3 + bn - 1) …(an + b1).

#include <iostream>
#include <locale.h>

using namespace std;

int main() {

    setlocale(LC_ALL, "Russian");

    int n;

    cout << "Введите n: ";
    cin >> n;

    double* a = new double[n];
    double* b = new double[n];

    cout << "Введите элементы a (0.0):" << endl;

    for (int i = 0; i < n; i++) {
        // Считывание i-го элемента массива a
        cin >> a[i];
    }


    cout << "Введите элементы b (0.0):" << endl;

    for (int i = 0; i < n; i++) {
        // Считывание i-го элемента массива b
        cin >> b[i];
    }
    // Инициализация переменной для хранения результата
    // Начальное значение 1.0, так как будем выполнять умножение
    double result = 1.0;

    // Цикл для вычисления произведения сумм элементов
    for (int i = 0; i < n; i++) {
        // Умножение текущего результата на сумму:
        // i-го элемента из a и (n-1-i)-го элемента из b
        // (элементы из b берутся с конца)
        result *= (a[i] + b[n - 1 - i]);
    }

    cout << "Результат: " << result << endl;

    delete[] a;
    delete[] b;

    return 0;
}
